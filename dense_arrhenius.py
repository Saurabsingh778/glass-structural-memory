# ─────────────────────────────────────────────────────────────────────────────
# FEATURE EXTRACTION  (identical to train.py)
# ─────────────────────────────────────────────────────────────────────────────

BOX_L    = float((256 / 1.2) ** (1.0 / 3.0))
RC_GRAPH = 1.5


def extract_features(positions):
    N   = len(positions)
    pos = np.asarray(positions, dtype=np.float32)
    dr   = pos[:, None, :] - pos[None, :, :]
    dr   = dr - BOX_L * np.round(dr / BOX_L)
    dist = np.sqrt(np.einsum("ijk,ijk->ij", dr, dr))
    nbr  = (dist > 1e-6) & (dist < RC_GRAPH)
    feats = np.zeros((N, 8), dtype=np.float32)
    coord = nbr.sum(axis=1).astype(np.float32)
    d_max = coord.max() if coord.max() > 0 else 1.0
    for i in range(N):
        nd = dist[i][nbr[i]]
        if len(nd) == 0:
            feats[i] = [RC_GRAPH, 0., RC_GRAPH, RC_GRAPH, 0., 0., RC_GRAPH, RC_GRAPH]
            continue
        mu  = nd.mean()
        sig = nd.std() if len(nd) > 1 else 0.0
        sk  = float(np.mean((nd - mu)**3) / (sig**3 + 1e-8)) if sig > 1e-8 else 0.0
        feats[i] = [mu, sig, nd.min(), nd.max(), coord[i]/d_max,
                    float(np.clip(sk, -5., 5.)),
                    float(np.percentile(nd, 25)), float(np.percentile(nd, 75))]
    mu_g  = feats.mean(axis=0, keepdims=True)
    sig_g = feats.std(axis=0,  keepdims=True) + 1e-8
    feats = (feats - mu_g) / sig_g
    rows, cols = np.where(nbr)
    ei = np.stack([rows, cols], axis=0).astype(np.int64)
    ea = dist[rows, cols].reshape(-1, 1).astype(np.float32)
    return feats, ei, ea


def load_graphs_at_cycle(pkl_path, cycle):
    with open(pkl_path, "rb") as f:
        samples = pickle.load(f)
    graphs, labels = [], []
    for s in samples:
        if s["fatigue_cycles"] != cycle:
            continue
        nf, ei, ea = extract_features(np.array(s["positions"], dtype=np.float32))
        if not (np.isfinite(nf).all() and np.isfinite(ea).all()):
            continue
        graphs.append(Data(
            x=torch.tensor(nf), edge_index=torch.tensor(ei),
            edge_attr=torch.tensor(ea),
            y=torch.tensor(float(s["label_cooling"])),
        ))
        labels.append(int(s["label_cooling"]))
    return graphs, np.array(labels)


# ─────────────────────────────────────────────────────────────────────────────
# MODEL  (matches train.py exactly)
# ─────────────────────────────────────────────────────────────────────────────

class GATv2Classifier(nn.Module):
    def __init__(self):
        super().__init__()
        self.enc  = nn.Linear(8, HIDDEN)
        self.gat1 = GATv2Conv(HIDDEN, HIDDEN, heads=HEADS, edge_dim=1, concat=True)
        self.gat2 = GATv2Conv(HIDDEN*HEADS, HIDDEN, heads=HEADS, edge_dim=1, concat=True)
        self.post = nn.Sequential(
            nn.Linear(HIDDEN*HEADS, 128), nn.LayerNorm(128), nn.ReLU(),
            nn.Dropout(0.2), nn.Linear(128, HIDDEN), nn.ReLU(),
        )
        self.head = nn.Linear(HIDDEN, 1)

    def forward(self, data):
        x, ei, ea, b = data.x, data.edge_index, data.edge_attr, data.batch
        h = F.relu(self.enc(x))
        h = F.relu(self.gat1(h, ei, ea))
        h = F.relu(self.gat2(h, ei, ea))
        return self.head(self.post(global_mean_pool(h, b))).squeeze(-1)


