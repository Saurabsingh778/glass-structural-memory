def stretched_exp(c, A_inf, A0, tau, beta):
    return A_inf + (A0 - A_inf) * np.exp(-np.power(np.maximum(c/tau, 1e-9), beta))


def fit_forgetting_curve(cycles, mean_accs, std_accs, T_val, label=""):
    c = np.array(cycles, dtype=float)
    y = np.array(mean_accs, dtype=float)
    w = 1.0 / (np.array(std_accs, dtype=float) + 0.5)

    # Estimate initial tau from where acc drops to 50% of total drop
    A0_est   = y[0]
    Ainf_est = float(np.percentile(y[-4:], 25))
    half     = (A0_est + Ainf_est) / 2.0
    tau_init = float(np.interp(half, y[::-1], c[::-1])) if A0_est > half else float(c[2])
    tau_init = max(0.5, min(tau_init, float(c[-1])))

    # Bounds adjusted for dense sampling (tau can be sub-cycle)
    lb = [Ainf_est - 15, A0_est - 10, 0.5,  0.05]
    ub = [Ainf_est + 15, A0_est + 5,  500., 3.0 ]

    try:
        popt, pcov = curve_fit(
            stretched_exp, c, y,
            p0=[Ainf_est, A0_est, tau_init, 0.7],
            bounds=(lb, ub), sigma=w, maxfev=50000
        )
        A_inf, A0, tau, beta = popt
        perr = np.sqrt(np.diag(pcov))
        y_pred = stretched_exp(c, *popt)
        r2 = max(0.0, 1 - np.sum((y-y_pred)**2) / max(np.sum((y-y.mean())**2), 1e-10))

        # Flag if hitting bounds
        at_lb = (tau < lb[2] + 0.1) or (beta < lb[3] + 0.01)
        converged = r2 > 0.5 and not at_lb

        print(f"\n  → τ={tau:.2f}±{perr[2]:.2f}  β={beta:.3f}  "
              f"A∞={A_inf:.1f}%  A0={A0:.1f}%  R²={r2:.4f}  "
              f"{'CONVERGED' if converged else 'AT BOUND — unreliable'}", flush=True)

        return dict(A_inf=float(A_inf), A_inf_err=float(perr[0]),
                    A0=float(A0),       A0_err=float(perr[1]),
                    tau=float(tau),     tau_err=float(perr[2]),
                    beta=float(beta),   beta_err=float(perr[3]),
                    r2=float(r2), converged=converged,
                    at_lower_bound=at_lb)
    except Exception as e:
        # Fallback linear interpolation
        tau_fb = tau_init
        print(f"\n  → curve_fit failed ({e}). Fallback τ≈{tau_fb:.1f}", flush=True)
        return dict(A_inf=float(Ainf_est), A_inf_err=5.0,
                    A0=float(A0_est), A0_err=2.0,
                    tau=tau_fb, tau_err=tau_fb*0.5,
                    beta=1.0, beta_err=0.5,
                    r2=0.0, converged=False, at_lower_bound=False)


