def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--dense_dir",  required=True,
                        help="Dir containing multihistory_dense_t38.pkl and "
                             "multihistory_dense_t4.pkl")
    parser.add_argument("--t35_pkl",    default=None,
                        help="Path to multihistory_glasses_amp_8_t_35_cycle_9.pkl "
                             "(if T=0.35 retraining needed)")
    parser.add_argument("--cache_pkl",  default=None,
                        help="Path to forgetting_curves.pkl from previous run "
                             "(reuses T=0.35 result if present)")
    parser.add_argument("--out_dir",    default="./dense_arrhenius_results")
    parser.add_argument("--skip_existing", action="store_true")
    args = parser.parse_args()
    os.makedirs(args.out_dir, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"  DENSE FORGETTING CURVE + ARRHENIUS FIT")
    print(f"  Device : {DEVICE}")
    if DEVICE.type == "cuda":
        print(f"  GPU    : {torch.cuda.get_device_name(0)}")
    print(f"{'='*60}\n")

    cache_out = os.path.join(args.out_dir, "dense_forgetting_curves.pkl")
    all_results = []

    # ── Load or compute T=0.35 ────────────────────────────────────────────
    t35_result = None

    # First priority: previous cache
    if args.cache_pkl and os.path.exists(args.cache_pkl):
        print(f"  Loading T=0.35 from previous cache: {args.cache_pkl}")
        with open(args.cache_pkl, "rb") as f:
            prev = pickle.load(f)
        for r in prev:
            if abs(r["T"] - 0.35) < 0.001:
                t35_result = r
                print(f"  Found T=0.35: τ={r['fit']['tau']:.2f}  "
                      f"β={r['fit']['beta']:.3f}  R²={r['fit']['r2']:.4f}")
                break

    # Second priority: retrain on sparse pkl
    if t35_result is None and args.t35_pkl and os.path.exists(args.t35_pkl):
        print(f"\n  No cache found — retraining T=0.35 from {args.t35_pkl}")
        t35_data = compute_forgetting_curve(
            args.t35_pkl, SPARSE_SAVE_AT, 0.35, "0.78Tg")
        t35_data["fit"] = fit_forgetting_curve(
            SPARSE_SAVE_AT, t35_data["mean_accs"],
            t35_data["std_accs"], 0.35, "T=0.35")
        t35_result = t35_data
    elif t35_result is None:
        print("\n  WARNING: No T=0.35 data available.")
        print("  Provide --cache_pkl or --t35_pkl for the full Arrhenius fit.")
        print("  Proceeding with T=0.38 and T=0.40 only (2-point Arrhenius).")

    if t35_result is not None:
        # Add fit if not present (cached result from old script may lack it)
        if "fit" not in t35_result:
            t35_result["fit"] = fit_forgetting_curve(
                t35_result["cycles"], t35_result["mean_accs"],
                t35_result["std_accs"], 0.35, "T=0.35")
        all_results.append(t35_result)

    # ── Compute dense forgetting curves ───────────────────────────────────
    if args.skip_existing and os.path.exists(cache_out):
        print(f"\n  Loading cached dense curves: {cache_out}")
        with open(cache_out, "rb") as f:
            dense_results = pickle.load(f)
        all_results.extend(dense_results)
    else:
        dense_results = []
        for T_val in [0.38, 0.40]:
            fname = DENSE_FILES[T_val]
            pkl_path = os.path.join(args.dense_dir, fname)
            if not os.path.exists(pkl_path):
                print(f"\n  WARNING: {pkl_path} not found — skipping T={T_val}")
                continue

            T_label = T_LABELS[T_val]
            r = compute_forgetting_curve(pkl_path, DENSE_SAVE_AT, T_val, T_label)
            r["fit"] = fit_forgetting_curve(
                DENSE_SAVE_AT, r["mean_accs"], r["std_accs"], T_val)
            dense_results.append(r)
            all_results.append(r)

        with open(cache_out, "wb") as f:
            pickle.dump(dense_results, f)
        print(f"\n  Cached dense results → {cache_out}")

    # ── Arrhenius fit ─────────────────────────────────────────────────────
    print(f"\n{'='*60}")
    print(f"  ARRHENIUS FIT")
    print(f"{'='*60}")
    arr = fit_arrhenius(all_results)

    # ── Figures ───────────────────────────────────────────────────────────
    # Sort by T for plotting
    all_results.sort(key=lambda r: r["T"])

    print(f"\n{'='*60}")
    print(f"  GENERATING FIGURES")
    print(f"{'='*60}")
    plot_forgetting_curves(all_results,
        os.path.join(args.out_dir, "fig_dense_forgetting.png"))

    if arr:
        plot_arrhenius(all_results, arr,
            os.path.join(args.out_dir, "fig_arrhenius.png"))

    # ── CSV summary ───────────────────────────────────────────────────────
    rows = []
    for r in all_results:
        fit = r.get("fit", {})
        rows.append({
            "T":       r["T"], "T/Tg": r["T_label"],
            "n_pts":   len(r["cycles"]),
            "cycle_range": f"{r['cycles'][0]}–{r['cycles'][-1]}",
            "A0 (%)":  f"{fit.get('A0', float('nan')):.1f}",
            "A_inf (%)": f"{fit.get('A_inf', float('nan')):.1f}",
            "tau":     f"{fit.get('tau', float('nan')):.2f}±{fit.get('tau_err', float('nan')):.2f}",
            "beta":    f"{fit.get('beta', float('nan')):.3f}",
            "R2":      f"{fit.get('r2', float('nan')):.4f}",
            "converged": fit.get("converged", False),
        })
    df = pd.DataFrame(rows)
    csv_path = os.path.join(args.out_dir, "arrhenius_summary.csv")
    df.to_csv(csv_path, index=False)

    # ── Final print ───────────────────────────────────────────────────────
    print(f"\n{'='*60}")
    print(f"  SUMMARY")
    print(f"{'='*60}")
    print(df.to_string(index=False))

    if arr:
        print(f"\n  ┌─────────────────────────────────────────────┐")
        print(f"  │  ACTIVATION ENERGY                          │")
        print(f"  │  Ea = {arr['Ea']:+.3f} ± {arr['Ea_err']:.3f} ε (LJ units)  │")
        print(f"  │  τ0 = {arr['tau0']:.2f} cycles                     │")
        print(f"  │  n  = {arr['n_points']} temperature points                │")
        print(f"  │  R² = {arr['r2']:.4f}                              │")
        print(f"  └─────────────────────────────────────────────┘")
        print(f"\n  Literature range [{arr['lit_lo']:.0f}–{arr['lit_hi']:.0f} ε]: "
              f"{'MATCH ✓' if arr['within_literature'] else 'OUTSIDE ⚠'}")
    else:
        print("\n  Arrhenius fit skipped (< 2 converged points)")

    print(f"\n  All outputs → {args.out_dir}/")


if __name__ == "__main__":
    main()
