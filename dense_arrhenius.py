def plot_forgetting_curves(all_results, out_path):
    n = len(all_results)
    fig, axes = plt.subplots(1, n, figsize=(5.5*n, 5.5), sharey=True)
    if n == 1:
        axes = [axes]

    colors = ["#3498db", "#e74c3c", "#2ecc71"]
    c_dense = np.linspace(0, max(r["cycles"][-1] for r in all_results), 300)

    for ax, res, col in zip(axes, all_results, colors):
        T_val = res["T"]
        cycles = np.array(res["cycles"], dtype=float)
        maccs  = np.array(res["mean_accs"])
        saccs  = np.array(res["std_accs"])
        fit    = res.get("fit")

        # Data points
        ax.errorbar(cycles, maccs, yerr=saccs,
                    fmt="o", color=col, capsize=4, markersize=8,
                    linewidth=1.8, zorder=5, label="GNN acc (5-fold CV)")

        # Fitted curve
        if fit and fit["converged"]:
            c_range = np.linspace(0, cycles[-1], 400)
            ax.plot(c_range,
                    stretched_exp(c_range, fit["A_inf"], fit["A0"],
                                  fit["tau"], fit["beta"]),
                    "-", color=col, lw=2.5, alpha=0.9,
                    label=f"Fit  $R^2={fit['r2']:.3f}$")
            ax.axvline(fit["tau"], color=col, ls="--", alpha=0.5, lw=1.2)

        ax.axhline(50, color="gray", ls=":", lw=0.8, alpha=0.5)

        # Annotation box
        if fit:
            status = "CONVERGED" if fit["converged"] else "AT BOUND"
            box_col = col if fit["converged"] else "#e67e22"
            txt = (f"$\\tau = {fit['tau']:.1f}\\pm{fit['tau_err']:.1f}$\n"
                   f"$\\beta = {fit['beta']:.2f}$\n"
                   f"$A_\\infty = {fit['A_inf']:.1f}\\%$\n"
                   f"$R^2 = {fit['r2']:.3f}$")
            ax.text(0.97, 0.97, txt, transform=ax.transAxes,
                    ha="right", va="top", fontsize=9,
                    bbox=dict(boxstyle="round,pad=0.4", fc="white",
                              ec=box_col, alpha=0.92))

        ax.set_title(f"T = {T_val}  ({res['T_label']})",
                     fontweight="bold", fontsize=11)
        ax.set_xlabel("Fatigue cycles", fontsize=10)
        ax.set_ylim(35, 102)
        ax.grid(alpha=0.3)
        ax.legend(fontsize=8.5)

    axes[0].set_ylabel("Cooling-rate accuracy (%)", fontsize=10)

    plt.suptitle(
        r"GNN Forgetting Curves:  $A_\infty + (A_0 - A_\infty)\,\exp[-(c/\tau)^\beta]$",
        fontsize=12, y=1.02)
    plt.tight_layout()
    plt.savefig(out_path, dpi=180, bbox_inches="tight", facecolor="white")
    plt.close()
    print(f"  Forgetting curve figure → {out_path}")


def plot_arrhenius(all_results, arr, out_path):
    fig = plt.figure(figsize=(13, 5.5))
    gs  = gridspec.GridSpec(1, 2, figure=fig, wspace=0.35)

    ax_arr = fig.add_subplot(gs[0])
    ax_tau = fig.add_subplot(gs[1])

    colors = {"0.35": "#3498db", "0.38": "#e74c3c", "0.40": "#2ecc71"}

    # ── Arrhenius plot ────────────────────────────────────────────────────
    T_vals_all = [r["T"] for r in all_results if r.get("fit")]
    tau_all    = [r["fit"]["tau"]     for r in all_results if r.get("fit")]
    terr_all   = [r["fit"]["tau_err"] for r in all_results if r.get("fit")]
    conv_all   = [r["fit"]["converged"] for r in all_results if r.get("fit")]

    for T, tau, terr, conv, res in zip(T_vals_all, tau_all, terr_all,
                                        conv_all, all_results):
        if not res.get("fit"):
            continue
        col = colors.get(str(T), "#7f8c8d")
        invT = 1.0 / T
        if tau > 0:
            rel_err = min(terr / tau, 1.5)
            ax_arr.errorbar(invT, np.log(tau), yerr=rel_err,
                            fmt="o" if conv else "s",
                            color=col, capsize=5, markersize=11,
                            markerfacecolor=col if conv else "white",
                            markeredgecolor=col, lw=2, zorder=5)
            ax_arr.annotate(f"T={T}\n({res['T_label']})",
                            (invT, np.log(tau)),
                            xytext=(6, 4), textcoords="offset points",
                            fontsize=8.5, color=col)

    if arr:
        # Fit line
        invT_range = np.linspace(min(1/T for T in T_vals_all)*0.97,
                                  max(1/T for T in T_vals_all)*1.03, 100)
        ax_arr.plot(invT_range,
                    arr["slope"]*invT_range + arr["intercept"],
                    "-", color="#2c3e50", lw=2.2,
                    label=f"Arrhenius  $R^2={arr['r2']:.3f}$  (n={arr['n_points']})")

        Ea = arr["Ea"]; Ea_err = arr["Ea_err"]
        box_col = "#27ae60" if arr["within_literature"] else "#e67e22"
        ax_arr.text(0.05, 0.95,
                    f"$E_a = {Ea:.2f} \\pm {Ea_err:.2f}\\,\\varepsilon$\n"
                    f"$\\tau_0 = {arr['tau0']:.2f}$ cycles\n"
                    f"Lit. range: [{arr['lit_lo']:.0f}, {arr['lit_hi']:.0f}] ε\n"
                    f"{'✓ Within expected range' if arr['within_literature'] else '⚠ See Discussion'}",
                    transform=ax_arr.transAxes, va="top", fontsize=10,
                    bbox=dict(boxstyle="round,pad=0.5", fc=box_col, alpha=0.15,
                              ec=box_col))
        ax_arr.legend(fontsize=9)

    ax_arr.set_xlabel("1/T  (LJ units)", fontsize=11)
    ax_arr.set_ylabel("log τ  (log cycles)", fontsize=11)
    ax_arr.set_title("(A) Arrhenius Plot", fontweight="bold")
    ax_arr.grid(alpha=0.3)

    # ── τ(T) panel ────────────────────────────────────────────────────────
    for T, tau, terr, conv, res in zip(T_vals_all, tau_all, terr_all,
                                        conv_all, all_results):
        if not res.get("fit"):
            continue
        col = colors.get(str(T), "#7f8c8d")
        ax_tau.errorbar(T, tau, yerr=terr,
                        fmt="o" if conv else "s", color=col,
                        capsize=5, markersize=11, lw=2,
                        markerfacecolor=col if conv else "white",
                        markeredgecolor=col, zorder=5)

    ax_tau.axvline(TG, color="black", ls="--", lw=1.3, alpha=0.6)
    ax_tau.text(TG+0.002, max(tau_all)*0.9, "$T_g = 0.45$", fontsize=9.5)
    ax_tau.set_xlabel("Cycling temperature T (LJ units)", fontsize=11)
    ax_tau.set_ylabel("τ (cycles)", fontsize=11)
    ax_tau.set_title("(B) Decay timescale τ(T)", fontweight="bold")
    ax_tau.set_yscale("log")
    ax_tau.grid(alpha=0.3)
    ax_tau.yaxis.set_minor_formatter(matplotlib.ticker.NullFormatter())

    if arr:
        plt.suptitle(
            f"Arrhenius Analysis of Thermal Memory Erasure\n"
            f"$E_a = {arr['Ea']:.2f} \\pm {arr['Ea_err']:.2f}\\,\\varepsilon$  "
            f"(LJ reduced units)",
            fontsize=12, y=1.02)

    plt.tight_layout()
    plt.savefig(out_path, dpi=180, bbox_inches="tight", facecolor="white")
    plt.close()
    print(f"  Arrhenius figure → {out_path}")


